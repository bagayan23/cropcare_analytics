What are Wilt Diseases?
Vascular System Disruption:
Wilt diseases primarily affect the vascular system (xylem and phloem) of plants, which are responsible for transporting water and nutrients. 
Pathogen Invasion:
Pathogens, such as fungi (like Fusarium and Verticillium), bacteria (like Ralstonia solanacearum), and nematodes, invade the vascular tissues, causing blockages or damage. 
Wilting Symptoms:
The inability of the plant to transport water and nutrients leads to characteristic symptoms like wilting (drooping leaves and stems), yellowing, and stunted growth. 
